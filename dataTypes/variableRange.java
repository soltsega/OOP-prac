package dataTypes;

public class variableRange {
    public static void main(String[] args) {
        // byte range is -128 to 127
        System.out.println("The range of byte data type is " + Byte.MIN_VALUE + " to " + Byte.MAX_VALUE);
        
        // short range is -32,768 to 32,767
        System.out.println("The range of short data type is " + Short.MIN_VALUE + " to " + Short.MAX_VALUE);
        
        // int range is -2,147,483,648 to 2,147,483,647
        System.out.println("The range of int data type is " + Integer.MIN_VALUE + " to " + Integer.MAX_VALUE);
        
        // long range is -9,223,372,036,854,775,808 to 9,223,372,036,854,775,807
        System.out.println("The range of long data type is " + Long.MIN_VALUE + " to " + Long.MAX_VALUE);

        // double range is -
        System.out.println("The range of double data type is " + Double.MIN_VALUE + " to " + Double.MAX_VALUE);

        // float range is -
        System.out.println("The range of float data type is " + Float.MIN_VALUE + " to " + Float.MAX_VALUE);
    }
}
