package specialChars;

public class specialCharacterscol {
    public static void main(String[] args) {
        System.out.println("This is a new line character: \nSee, it moved to the next line!");
        System.out.println("This is a tab character:\tSee, it added a tab space!");
        System.out.println("This is a backslash character: \\");
        System.out.println("This is a double quote character: \"Hello\"");
        System.out.println("This is a single quote character: \'Hello\'");
        System.out.println("This is a carriage return character: Hello\rWorld");
        System.out.println("This is a backspace character: Hello\bWorld");
        System.out.println("This is a form feed character: Hello\fWorld");
        System.out.println("This is a null character: Hello\0World");
    }
}